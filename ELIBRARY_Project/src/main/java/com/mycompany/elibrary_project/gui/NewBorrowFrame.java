    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            // Retrieve selected items from combo boxes and date picker
            Book selectedBook = (Book) bookCmb.getSelectedItem();
            Member selectedMember = (Member) memberCmb.getSelectedItem();
            Staff selectedStaff = (Staff) staffCmb.getSelectedItem();
            Date selectedDate = (Date) dateCmb.getDate();

            // Check if any of the fields are empty or invalid
            if (selectedBook == null || selectedMember == null || selectedStaff == null
                    || selectedBook.getId() == null || selectedMember.getId() == null || selectedStaff.getId() == null) {
                JOptionPane.showMessageDialog(this, "Please fill all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Check if the date is valid (not null)
            if (selectedDate == null) {
                JOptionPane.showMessageDialog(this, "Please select a borrow date!", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Create the Borrow object and set the properties
            Borrow borrow = new Borrow();
            borrow.setBook(selectedBook);
            borrow.setMember(selectedMember);
            borrow.setStaff(selectedStaff);
            borrow.setBorrowDate(selectedDate);
            borrow.setActive(1); // Assuming 1 means active in your system

            // Call the service to add the borrow record
            borrowService.addBorrow(borrow);

            // Show success message
            JOptionPane.showMessageDialog(this, "Borrow has been successfully added!");
            this.dispose(); // Close the frame after saving

        } catch (NullPointerException ex) {
            // Handle specific null pointer exception, if needed
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: Missing required fields!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            // Catch any other exceptions
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error while adding borrow!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_saveBtnActionPerformed

