    private void showBookComboData() {
        try {
            DefaultComboBoxModel bookCombo = new DefaultComboBoxModel();
            bookCmb.setModel(bookCombo);
            bookCombo.addElement("Select Book");
            List<Book> bookList = bookService.getBookList();

            for (Book book : bookList) {
                bookCombo.addElement(book);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

