    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        try {
            String name = nameTxt.getText();
            String surname = surnameTxt.getText();
            String phone = phoneTxt.getText();

            Member member = memberService.getMemberById(selectedRowId);
            member.setName(name);
            member.setSurname(surname);
            member.setPhone(phone);

            memberService.updateMember(member);
            JOptionPane.showInternalMessageDialog(null, "Member has been successfully updated!");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Member has not been updated!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

