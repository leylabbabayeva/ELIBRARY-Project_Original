    private void showMemberOldData() {
        try {
            Member member = memberService.getMemberById(selectedRowId);
            nameTxt.setText(member.getName());
            surnameTxt.setText(member.getSurname());
            phoneTxt.setText(member.getPhone());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
